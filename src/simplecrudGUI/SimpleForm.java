    public void ShowData(){
        String kolom[] = {"ID Member", "Nama Member", "Username", "Email", "Umur", "Alamat", "No. HP"};
        dtm = new DefaultTableModel(null, kolom);
        try {
            Statement stmt = koneksi.createStatement();
            String query = "SELECT * FROM tb_member";
            System.out.println(query);
            ResultSet rs = stmt.executeQuery(query);
            while(rs.next()){
                String id = rs.getString("id_member");
                String nm = rs.getString("nm_member");
                String user = rs.getString("username");
                String mail = rs.getString("email");
                String age = rs.getString("umur");
                String address = rs.getString("alamat");
                String hp = rs.getString("no_hp");
                dtm.addRow(new String[]{id, nm, user, mail, age, address, hp});
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Kesalahan Pada Database");
        }
        tbl_member.setModel(dtm);
    }
    
