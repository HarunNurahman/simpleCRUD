    public void editData() {
        String id = tbl_member.getValueAt(baris, 0).toString();
        if (baris == -1) {
            JOptionPane.showMessageDialog(null, "Data Belum Dipilih");
        } else {
            new ManageMember(this, true, "edit", id).setVisible(true);
        }
    }
    
    public void deleteData(){
        String id = tbl_member.getValueAt(baris, 0).toString();
        if(baris == -1){
            JOptionPane.showMessageDialog(null, "Data Belum Dipilih");
        } else {
            try {
                Statement stmt = koneksi.createStatement();
                String query = "DELETE FROM tb_member WHERE id_member = '" + id + "'";
                int berhasil = stmt.executeUpdate(query);
                if(berhasil == 1){
                    JOptionPane.showMessageDialog(null, "Member Berhasil Dihapus");
                    dtm.getDataVector().removeAllElements();
                    ShowData();
                } else {
                    JOptionPane.showMessageDialog(null, "Member Gagal Dihapus");
                }
            } catch (SQLException e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, "Kesalahan Pada DB atau Query");
            }
        }
    }

